/* Comencemos con nuestro primer programa hola_mundo.dart
  el programa consiste en imprimir por consola de comandos el saludo
  ¡Hola Mundo! */

// Aquí es donde la aplicación comienza a ejecutarse.
void main() {
  String saludo = "¡Hola Mundo!"; // Declaración e inicialización de variable.
  print(saludo); // Llamada a una función
}
// Aquí es donde la aplicación termina.

/* Felicidades ya escribiste tu primer programa en Dart.
   Ahora comenzaremos a explicar las partes que componen el programa.

    Void                -> Es un tipo especial que indica un valor que nunca se usa.

    main()              -> Es una función especial, requerida y de nivel superior
                           donde comienza la ejecución de la aplicación.

    String              -> Es una cadena (objeto String) que contiene una
                           secuencia de unidades de código UTF-16. Se puede usar
                           comillas simples o dobles para crear una cadena.
    
    saludo              -> Es el nombre de la variable.

    =                   -> Es el operador de asignación.

    "¡Hola Mundo!"      -> Es el valor de la variable.

    print(saludo)       -> Es una función que imprime el valor de la variable
                           saludo en la consola de comandos.
*/

